export function summarizeEngagementData(data: EngagementData[]): {
  postType: string;
  totalLikes: number;
  totalComments: number;
  totalShares: number;
}[] {
  const summary = data.reduce((acc, { Post_Type, Likes, Comments, Shares }) => {
    if (!acc[Post_Type]) {
      acc[Post_Type] = { totalLikes: 0, totalComments: 0, totalShares: 0 };
    }
    acc[Post_Type].totalLikes += Likes;
    acc[Post_Type].totalComments += Comments;
    acc[Post_Type].totalShares += Shares;
    return acc;
  }, {} as Record<string, { totalLikes: number; totalComments: number; totalShares: number }>);

  return Object.entries(summary).map(
    ([postType, { totalLikes, totalComments, totalShares }]) => ({
      postType,
      totalLikes,
      totalComments,
      totalShares,
    })
  );
}
